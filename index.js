const chalk = require('chalk');
const colorList = [
  '#ff0000',
  '#666666',
  '#999999',
  '#efefef',
  '#b7b7b7',
  '#444444',
];

if (process.argv.length === 3) {
  console.log(
    chalk.keyword(process.argv[2])(`    ###############################
    ###############################
    ###############################
    ###############################
    ###          ${process.argv[2]}         ###
    ###############################
    ###############################
    ###############################p
    ###############################`),
  );
} else {
  const nbr = colorList[Math.floor(Math.random() * colorList.length)];
  console.log(
    chalk.hex(nbr)(`    ###############################
    ###############################
    ###############################
    ###############################
    ###          ${nbr}        ###
    ###############################
    ###############################
    ###############################
    ###############################`),
  );
}
